
    .global _start       // 声明 _start 标签为全局符号

_start:
    // 初始化数据
    mov x0, #5          // 将数字 5 存储到寄存器 x0
    mov x1, #10         // 将数字 10 存储到寄存器 x1

    // 加法操作: sum = x0 + x1
    add x2, x0, x1      // 将 x0 和 x1 的值相加，并将结果存储到 x2

    // 乘法操作: product = x0 * x1
    mul x3, x0, x1      // 将 x0 和 x1 的值相乘，并将结果存储到 x3

    // 输出结果 (这里只是将值保存在寄存器中，实际输出会使用系统调用)

    // 假设 sum (存储在 x2) 和 product (存储在 x3) 需要返回给操作系统
    mov x8, #93         // 系统调用号 93 是 exit
    mov x0, #0          // exit 状态码 0
    svc #0              // 调用系统服务
